fruit = ["🍏", "🍐", "🍊", "🍋", "🍌", "🍉", "🍇", "🍓", "🍈", "🍒", "🍑", "🍍"]

# forEach loop
for f in fruit:
    print(f)

# loop from 0 to 3 exclusive # 0, 1, 2
for i in range(3):
    print(i)


# loop from 2 to 5 exclusive # 2, 3, 4
for i in range(2, 5):
    print(i)

# loop from 1 to 10 exclusive with step 2 # 1, 3, 5, 7, 9
for i in range(1, 10, 2):
    print(i)

# run 3 times:
for _ in range(3):
    print("Hello")
